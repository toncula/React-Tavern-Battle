export const getTranslation = (lang: Language) => TRANSLATIONS[lang] || TRANSLATIONS['en'];

export const getCardText = (card: CardData, lang: Language) => {
    const t = getTranslation(lang).cards;
    if (card.templateId && card.templateId in t) {
        return t[card.templateId as keyof typeof t];
    }
    return { name: card.name, desc: card.description };
};

export const getUpgradeText = (upgrade: any, lang: Language) => {
    return { name: upgrade.name, desc: upgrade.description };
};

export const getEffectText = (effectId: SpecialEffectId, lang: Language) => {
    const t = getTranslation(lang);
    return t.effects[effectId];
};

export const getUnitTypeText = (type: UnitType, lang: Language) => {
    const t = getTranslation(lang);
    return t.unitTypes[type];
};